package com.laughing.lang.exceptions;

import com.laughing.lang.LaughingException;

public class FormConfigException extends LaughingException {

	private static final long serialVersionUID = -9206376423960980927L;

	public FormConfigException() {
		super();
	}

	public FormConfigException(String message, Throwable cause) {
		super(message, cause);
	}

	public FormConfigException(String message) {
		super(message);
	}

	public FormConfigException(Throwable cause) {
		super(cause);
	}

}
