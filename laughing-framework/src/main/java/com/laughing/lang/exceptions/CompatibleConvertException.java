package com.laughing.lang.exceptions;

public class CompatibleConvertException extends MagicException {

	private static final long serialVersionUID = -5918394699299390077L;

	public CompatibleConvertException() {
		super();
	}

	public CompatibleConvertException(String message, Throwable cause) {
		super(message, cause);
	}

	public CompatibleConvertException(String message) {
		super(message);
	}

	public CompatibleConvertException(Throwable cause) {
		super(cause);
	}

}
